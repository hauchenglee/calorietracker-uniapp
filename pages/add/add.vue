        // ÊéßÂà∂ËæìÂÖ•Êï∞Èáè start
        limitQuantityInput(event, formName) {
            // uniapp‰∏≠event.detail.valueËé∑ÂèñËæìÂÖ•ÂÄº
            let value = event.detail.value;

            // Â¶ÇÊûúËæìÂÖ•‰∏∫Á©∫ÔºåËÆæ‰∏∫1
            if (!value) {
                this[formName].quantity = 1;
