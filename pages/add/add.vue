                content: 'Á°ÆÂÆöË¶ÅÂèñÊ∂àÈÅ∏ÂèñÁï∂ÂâçÂúñÁâáÂêóÔºü',
                success: (res) => {
                    if (res.confirm) {
                        this.previewImage = '';
                        this.analysisCompleted = false; // ÈáçÁΩÆÂàÜÊûêÁä∂ÊÄÅ
                        this.formData1 = {  // Ê∏ÖÁ©∫Ë°®ÂçïÊï∞ÊçÆ
