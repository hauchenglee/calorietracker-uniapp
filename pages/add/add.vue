                this.analysisCompleted = false;
                this.formData1 = {
                    name: '',
                    meal: '',
                    quantity: '',
                    calorie: '',
                    carbohydrate: '',
                    protein: '',
                    fat: ''
                };
            } finally {
                this.isAnalyzing = false;
                uni.hideLoading();
            }
        },

